<?php
namespace CodeOrders\V1\Rest\Orders;

use ZF\ApiProblem\ApiProblem;
use ZF\Rest\AbstractResourceListener;

class OrdersResource extends AbstractResourceListener
{

    /**
     * @var OrdersServices
     */
    private $services;

    /**
     * @var OrdersRepository
     */
    private $repository;
    

    public function __construct(OrdersRepository $repository, OrdersService $services)
    {
        $this->repository = $repository;
        
        $this->services = $services;
    }

    /**
     * Create a resource
     *
     * @param  mixed $data
     * @return ApiProblem|mixed
     */
    //Metodo responsavel por inserir na tabela da base de dados
    public function create($data)
    {
        //return $this->repository->insert($data);
        $result = $this->services->insert($data);
        if ($result == 'error'){
            return new ApiProblem(405, 'Erro ao processar a ordem!');
        }
        return $result;
    }

    /**
     * Delete a resource
     *
     * @param  mixed $id
     * @return ApiProblem|mixed
